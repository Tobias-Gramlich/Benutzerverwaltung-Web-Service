const validateEmail = (email) => {
    //* Regex for valid EMail Format
    const regexEmail = new RegExp("^[A-Za-zÄÖÜäöüß0-9]+@(?:[A-Za-zÄÖÜäöüß]+\.)+[A-Za-zÄÖÜäöüß]+$");

    //* Max and Min Length
    const maxLength = 64;
    const minLength = 8;

    //* Test for valid Format
    if (!regexEmail.test(email)) return {error: "EMail has no a valid format"};
    if (email.length < minLength) return {error: `EMail must be longer than ${minLength - 1} Characters`};
    if (email.length > maxLength) return {error: `EMail must be shorter than ${maxLength + 1} Characters`};
    
    //* Return Success
    return {success: true};
};

module.exports = {validateEmail};